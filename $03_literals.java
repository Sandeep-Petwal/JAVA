public class $03_literals {
    public static void main(String[] args) {
        // Any constant value which can be assigned to the variable is called
        // literal/constant.In simple words, Literals in Java is a synthetic
        // representation of boolean, numeric, character, or string data. It is a medium
        // of expressing particular values in the program, such as an integer variable
         
        byte age = 23;
        System.out.println(age);
        int age1 = 5777;
        System.out.println(age1);
        short age2 = 2;
        System.out.println(age2);
        long big = 4343434343434l;
        System.out.println(big);
        char c = 's';
        System.out.println(c);
        float f1 = 23.2f;
        System.out.println(f1);
        double d1 = 453.34d;
        System.out.println(d1);
        boolean b = true;
        System.out.println(b);
        String name = "sanjup";
        System.out.println(name);
    }
}
